    public void llenarComboDep() {
        ArrayList<Departamento> departamentos = this.depController.getDepartamentos();
        
        for(Departamento dep : departamentos) {
            this.cmbDepartamentoResi.addItem(dep);
            this.cmbDepartNaci.addItem(dep);
            this.cmbDepaExpe.addItem(dep);
        }
        
    }
    
    public Votante llenarVotante() {
        
        Municipio muni = (Municipio) this.cmbMunicipioResi.getSelectedItem();
            int MuniId = muni.getId();
            
            Votante vot = new Votante();
            vot.setNombres(this.txtNombres.getText().trim());
            vot.setApellidos(this.txtApellidos.getText().trim());
            vot.setSexo( this.radMasculino.isSelected() ? "M" : "F" );
            vot.setDUI(this.txtDui.getText());
            vot.setDireccion(this.txtDireccion.getText());
            vot.setConocidoPor(this.jTxtConocidoPor.getText());
            vot.setSalvadorenoPor( this.cmbNacionalidad.getSelectedItem().toString() );
            vot.setFechaNacimiento(this.dateNacimiento.getDateStringOrEmptyString());
            vot.setDepartamentoNaci(this.cmbDepartNaci.getSelectedItem().toString());
            vot.setMunicipioNaci(this.cmbMuniNaci.getSelectedItem().toString());
            vot.setFechaExpedicion(this.dateDuiExpedicion.getDateStringOrEmptyString());
            vot.setDepartamentoExpe(this.cmbDepaExpe.getSelectedItem().toString());
            vot.setMunicipioExpe(this.cmbMuniExpe.getSelectedItem().toString());
            vot.setFechaExpiracion(this.dateDuiExpiracion.getDateStringOrEmptyString());
            vot.setDepartamentoResidencia(this.cmbDepartamentoResi.getSelectedItem().toString());
            vot.setMunicipioResidencia( MuniId );
            vot.setNIT(this.jTxtNit.getText());
            vot.setNombreMadre(this.jTxtNombreMadre.getText());
            vot.setNombrePadre(this.jTxtNombrePadre.getText());
            vot.setCodigoZona(this.jTxtcodZona.getText());
            vot.setEstadoFamiliar(this.cmbEstadoFamliar.getSelectedItem().toString());
            vot.setConyugeNombre(this.jTxtConyuge.getText());
            vot.setTipoSangre(this.cmbTipoSangre.getSelectedItem().toString());
            vot.setProfesion(this.cmbProfesion.getSelectedItem().toString());
        
        return vot;
    }
    
    
