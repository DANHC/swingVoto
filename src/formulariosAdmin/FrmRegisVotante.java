    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        
        
        if( this.validator.emptyFields(this.getContentPane()) && !this.txtNombres.getText().trim().isEmpty()
                && !this.txtApellidos.getText().trim().isEmpty() 
                && !this.dateNacimiento.getDateStringOrEmptyString().isEmpty() 
                && !this.dateDuiExpiracion.getDateStringOrEmptyString().isEmpty() 
                && !this.dateDuiExpedicion.getDateStringOrEmptyString().isEmpty() 
                && this.txtDui.isEditValid() 
                && !this.txtDireccion.getText().trim().isEmpty() 
                && !this.jTxtNombreMadre.getText().trim().isEmpty()
                && !this.jTxtNombrePadre.getText().trim().isEmpty() 
                && this.jTxtNit.isEditValid() 
                && !this.jTxtcodZona.getText().trim().isEmpty()
                )
        {
            
            String msj = this.votC.insertarVotante(this.llenarVotante());
            JOptionPane.showMessageDialog(this, msj, "Notificacion", 2); 
            
        } else if( !this.validator.emptyFields(this.getContentPane()) ) {
            String msj = this.votC.insertarVotante(this.llenarVotante());
            JOptionPane.showMessageDialog(this, msj, "Notificacion", 2);
        } else {
            JOptionPane.showMessageDialog(this, "Llene los campos obligatorios", "Error", 2);
        }
        
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

    
    
    
