    private void cmbDepartamentoResiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDepartamentoResiItemStateChanged
        Departamento dep =  (Departamento) this.cmbDepartamentoResi.getSelectedItem();
        ArrayList<Municipio> municipios = this.depController.getMunicipios(dep.getId());
        this.cmbMunicipioResi.removeAllItems();
        
        for(Municipio muni : municipios) {
            this.cmbMunicipioResi.addItem(muni);
        }
        
    }//GEN-LAST:event_cmbDepartamentoResiItemStateChanged

