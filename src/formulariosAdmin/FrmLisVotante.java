    public void llenarCombo() {
        for(Departamento dep : this.combC.getDepartamentos()) {
            this.cmbDepartamento.addItem(dep);
        }
    }
    
    public void llenarTabla(String dui) {
        String duiS = dui.equals("        - ") ? "" : dui;
        this.tablaPadron.setModel( this.padC.getPadronDui(duiS) );
        TableColumnModel tcm = this.tablaPadron.getColumnModel();
        tcm.removeColumn( tcm.getColumn(0) );
    }
    
    public void llenarTablaMuni(int idMuni) {
        this.tablaPadron.setModel( this.padC.getPadronMuni(idMuni) );
        TableColumnModel tcm = this.tablaPadron.getColumnModel();
        tcm.removeColumn( tcm.getColumn(0) );
    }
    
