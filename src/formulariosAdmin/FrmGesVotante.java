    public void llenarTabla(String dui) {
        String duiS = dui.equals("        - ") ? "" : dui;
        
        this.tablaVotantes.setModel( this.votanteC.getVotantes(duiS) );
        TableColumnModel tcm = this.tablaVotantes.getColumnModel();
        tcm.removeColumn( tcm.getColumn(0) );
        tcm.removeColumn( tcm.getColumn(0) );
        tcm.removeColumn( tcm.getColumn(3) );
        tcm.removeColumn( tcm.getColumn(4) );
        tcm.removeColumn( tcm.getColumn(4) );
        tcm.removeColumn( tcm.getColumn(4) );
    }
    
    public Votante llenarVotante() {
        int fila = this.tablaVotantes.getSelectedRow();
        String dui = String.valueOf(this.tablaVotantes.getModel().getValueAt(fila, 2));
        Municipio muni = (Municipio) this.cmbMunicipio.getSelectedItem();
        int muniId = muni.getId();
        
        Votante vot = new Votante();
        vot.setDUI(dui);
        vot.setNombres(this.txtNombres.getText());
        vot.setApellidos(this.txtApellidos.getText());
        vot.setDireccion(this.txtDireccion.getText());
        vot.setDepartamentoResidencia(this.cmbDepartamento.getSelectedItem().toString());
        vot.setMunicipioResidencia(muniId);
        
        return vot;
    }
    
    
