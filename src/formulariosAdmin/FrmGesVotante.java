    private void tablaVotantesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaVotantesMouseClicked
        int fila = this.tablaVotantes.getSelectedRow();
        
        String depNombre = (String) this.tablaVotantes.getModel().getValueAt(fila, 7);
        int depId = (Integer) this.tablaVotantes.getModel().getValueAt(fila, 0);
        String munNombre = (String) this.tablaVotantes.getModel().getValueAt(fila, 8);
        int munId = (Integer) this.tablaVotantes.getModel().getValueAt(fila, 1);
        
        String estado = (String) this.tablaVotantes.getModel().getValueAt(fila, 9);
        
        Departamento dep = new Departamento(depId, depNombre);
        Municipio muni = new Municipio(munId, munNombre);
        
        
        this.cmbDepartamento.getModel().setSelectedItem(dep);
        this.cmbEstado.getModel().setSelectedItem(estado);
        this.cmbMunicipio.getModel().setSelectedItem(muni);
        
        this.txtNombres.setText( (String) this.tablaVotantes.getModel().getValueAt(fila, 3));
        this.txtApellidos.setText( (String) this.tablaVotantes.getModel().getValueAt(fila, 4) );
        this.txtDireccion.setText( (String) this.tablaVotantes.getModel().getValueAt(fila, 5));
        
    }//GEN-LAST:event_tablaVotantesMouseClicked


